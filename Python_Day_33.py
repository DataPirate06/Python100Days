#Dictionaries in python
info = {'name':'Karan', 'age':19, 'eligible':True}
print(info)

info = {'name':'Karan', 'age':19, 'eligible':True}
print(info['name'])
print(info.get('name'))

info = {'name':'Karan', 'age':19, 'eligible':True}
print(info.values())

info = {'name':'Karan', 'age':19, 'eligible':True}
print(info.keys())

info = {'name':'Karan', 'age':19, 'eligible':True}
print(info.items())
print(type(info.items()))